# typed: false
# frozen_string_literal: true

require "spec_helper"
require "dependabot/python"

RSpec.describe Dependabot::Python::PipCompileFileMatcher do
  subject(:matcher) { described_class.new(requirements_in_files) }

  let(:requirements_in_file) do
    Dependabot::DependencyFile.new(
      name: "requirements.in",
      content: "requests==2.32.5\n",
      directory: "/"
    )
  end

  let(:requirements_in_files) { [requirements_in_file] }

  describe "#manifest_for_pip_compile_lockfile" do
    context "when lockfile has a matching .in basename" do
      let(:lockfile) do
        Dependabot::DependencyFile.new(
          name: "requirements.txt",
          content: "requests==2.32.5\n",
          directory: "/"
        )
      end

      it "returns the matching manifest file" do
        expect(matcher.manifest_for_pip_compile_lockfile(lockfile)).to eq(requirements_in_file)
      end
    end

    context "when lockfile has no matching .in basename" do
      let(:other_manifest) do
        Dependabot::DependencyFile.new(
          name: "base.in",
          content: "requests==2.32.5\n",
          directory: "/"
        )
      end

      let(:requirements_in_files) { [other_manifest] }

      let(:lockfile) do
        Dependabot::DependencyFile.new(
          name: "requirements.txt",
          content: <<~TXT,
            # This file is autogenerated by pip-compile
            #    pip-compile --output-file=requirements.txt base.in
            requests==2.32.5
          TXT
          directory: "/"
        )
      end

      it "falls back to the first available manifest" do
        expect(matcher.manifest_for_pip_compile_lockfile(lockfile)).to eq(other_manifest)
      end
    end

    context "when file is not a pip-compile lockfile" do
      let(:lockfile) do
        Dependabot::DependencyFile.new(
          name: "Pipfile.lock",
          content: "{}",
          directory: "/"
        )
      end

      it "returns nil" do
        expect(matcher.manifest_for_pip_compile_lockfile(lockfile)).to be_nil
      end
    end
  end
end
